package movie;

public class MovieVO {
	private String docid;
	private String title;
	private String directors;
	private String actors;
	private String nation;
	private String genre;
	private String plots;
	private String repRlsDate; 
	private String poster;
	private int runtime;
	private String rating_grade;
	private int rating;
	private int rating_people;
	
	public String getDocid() {
		return docid;
	}
	public void setDocid(String docid) {
		this.docid = docid;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDirectors() {
		return directors;
	}
	public void setDirectors(String directors) {
		this.directors = directors;
	}
	public String getActors() {
		return actors;
	}
	public void setActors(String actors) {
		this.actors = actors;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getPlots() {
		return plots;
	}
	public void setPlots(String plots) {
		this.plots = plots;
	}
	public String getRepRlsDate() {
		return repRlsDate;
	}
	public void setRepRlsDate(String repRlsDate) {
		this.repRlsDate = repRlsDate;
	}
	public String getPoster() {
		return poster;
	}
	public void setPoster(String poster) {
		this.poster = poster;
	}
	public int getRuntime() {
		return runtime;
	}
	public void setRuntime(int runtime) {
		this.runtime = runtime;
	}
	public String getRating_grade() {
		return rating_grade;
	}
	public void setRating_grade(String rating_grade) {
		this.rating_grade = rating_grade;
	}
	public int getRating() {
		return rating;
	}
	public void setRating(int rating) {
		this.rating = rating;
	}
	public int getRating_people() {
		return rating_people;
	}
	public void setRating_people(int rating_people) {
		this.rating_people = rating_people;
	}
	
}
